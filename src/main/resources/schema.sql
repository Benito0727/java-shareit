CREATE TABLE IF NOT EXISTS users (
user_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
user_name VARCHAR(64) NOT NULL,
user_email VARCHAR(128) NOT NULL UNIQUE
);

CREATE TABLE IF NOT EXISTS items (
item_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
item_name VARCHAR(64) NOT NULL,
item_description VARCHAR(512) NOT NULL
);

CREATE TABLE IF NOT EXISTS item_available (
item_id BIGINT NOT NULL REFERENCES items(item_id) ON UPDATE CASCADE ON DELETE CASCADE,
item_available BOOLEAN NOT NULL
);

CREATE TABLE IF NOT EXISTS users_items (
user_id BIGINT NOT NULL REFERENCES users(user_id) ON UPDATE CASCADE ON DELETE CASCADE,
item_id BIGINT NOT NULL REFERENCES items(item_id) ON UPDATE CASCADE ON DELETE CASCADE
);